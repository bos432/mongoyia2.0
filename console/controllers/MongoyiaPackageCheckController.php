    public function actionRun()
    {
        $this->stdout("Mongoyia handover package check\n");

        $this->section('PHP project');
        foreach ($this->phpFiles() as $path) {
            $this->requireFile($this->projectRoot() . DIRECTORY_SEPARATOR . $path, $path);
        }

        $this->section('Python IM project');
        $imRoot = $this->resolvePath($this->imRoot);
        foreach ($this->imFiles() as $path) {
            $this->requireFile($imRoot . DIRECTORY_SEPARATOR . $path, $this->imRoot . '/' . $path);
        }

        $this->stdout("\nSummary: {$this->failures} failure(s).\n");
        return $this->failures > 0 ? ExitCode::UNSPECIFIED_ERROR : ExitCode::OK;
    }

