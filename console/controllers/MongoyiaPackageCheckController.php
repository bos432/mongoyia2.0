    private function requireFile(string $path, string $label)
    {
        if (is_file($path)) {
            $this->stdout("OK   {$label}\n");
            return;
        }

        $this->failures++;
        $this->stderr("FAIL missing {$label}\n");
    }

    private function resolvePath(string $path)
    {
        if (preg_match('/^[A-Za-z]:[\\\\\\/]/', $path)) {
            return $path;
        }

        return $this->projectRoot() . DIRECTORY_SEPARATOR . $path;
    }

    private function projectRoot()
    {
        return dirname(__DIR__, 2);
    }

    private function section(string $name)
    {
        $this->stdout("\n[{$name}]\n");
    }
